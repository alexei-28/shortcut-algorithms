/*
    https://shortcut.education/page119537466.html#!/tab/1948616871-2
    Упражнения к модулю 2: Базовые структуры данных
    Уровень 2: Базовые операции
    Задание 3: Поиск индекса элемента
    Напишите функцию, которая ищет индекс первого вхождения элемента в массиве. Если элемент не найден, верните -1.
    Примеры:
        Вход: arr = [1, 2, 3, 4, 5], value = 3
        Выход: 2

        Вход: arr = [1, 2, 3, 4, 5], value = 6
        Выход: -1

    Требования:
    - Временная сложность: O(n)
    - Пространственная сложность: O(1)
    - Не используйте встроенные методы поиска

    Решение:
    O(n) время, O(1) память
    Пояснение:
    Время: O(n) — проходим по массиву один раз в худшем случае (элемент отсутствует или находится в конце)
    Память: O(1) — используем только переменные i и возвращаемое значение, не зависит от размера массива

    Данный код представляет собой классический алгоритм линейного поиска (Linear Search).
    Его временная сложность описывается как O(n), где n — это количество элементов в массиве arr.
    1. Анализ по шагамЧтобы определить сложность, мы смотрим, сколько элементарных операций выполняет
       процессор относительно размера входных данных (n):
        - Инициализация цикла: int index = 0 выполняется 1 раз.
        - Условие выхода: index < arr.length проверяется до n + 1 раз.
        - Инкремент: index++ выполняется до n раз.
        - Сравнение: if (arr[index] == item) выполняется до n раз.

        Поскольку количество операций растет пропорционально количеству элементов в массиве, мы имеем дело с линейной зависимостью.

    2. Почему именно O(n)
       В анализе алгоритмов нас обычно интересует худший сценарий, так как он гарантирует, что программа не будет работать дольше
       определенного времени. Если массив увеличится в 10 раз, время выполнения программы в худшем случае также увеличится примерно в 10 раз.
       Именно эту прямую зависимость и обозначает символ O(n).

    3. Пространственная сложность (Space Complexity)
       Помимо времени, стоит отметить и затраты памяти. В данном методе:
        - Мы не создаем новые структуры данных (массивы или списки).
        - Используется лишь одна переменная index.
       Следовательно, пространственная сложность O(1), так как объем используемой памяти не зависит от размера входящего массива.
*/
public class Task3 {

    public int solution(int[] arr, int item) {
        for (int index = 0; index < arr.length; index++) {
            if (arr[index] == item) {
                return index;
            }
        }
        return -1;
    }
}
